# 입력을 받기위한 input 함수는 절대 사용하지 않습니다.
def over(score_list):

    # 횟수를 0으로 설정한다.
    count = 0
    # 배열을 순회하며 60 이상의 숫자일 경우 횟수를 1 증가
    for score in score_list:
        if score >= 60:
            count+=1
    # 함수의 return 값으로 횟수 전달
    return count


